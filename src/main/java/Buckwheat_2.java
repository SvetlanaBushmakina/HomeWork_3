public class Buckwheat_2 {
    public static void main(String[] args) {
        int month = 9;
        int monthBuckwheat = 6;
        int payForMonth;
        int allBuckwheat = monthBuckwheat * month;
        int allPay = allBuckwheat * 100;
        for (; allBuckwheat <= allBuckwheat; ) {
            allBuckwheat = allBuckwheat - monthBuckwheat;
            if (allBuckwheat < 0) {

                break;
            }
            payForMonth = allBuckwheat * 100;
            allPay += payForMonth;
        }
        System.out.println(allPay);
    }
}

